def export_openai_agents(project) -> dict:
    """Emit an OpenAI Agents SDK script. Subagents become tools on the coordinator
    via ``as_tool()`` (confirmed working in experiments/subagent-composition). The
    orchestration loop runs in YOUR process -- the ``emulated`` subagents tier."""
    o = CAPABILITIES["openai"]
    out = [
        '"""Generated by `agentlift export openai-agents` - PREVIEW.',
        "",
        "Maps the neutral agent folder to an OpenAI Agents SDK script. Subagents become",
        "tools on the coordinator via as_tool() (confirmed working - see",
        "experiments/subagent-composition). The orchestration loop runs in THIS process,",
        "not OpenAI-hosted: that is the `emulated` subagents tier agentlift audit reports.",
        '"""',
        "import asyncio",
        "",
        "from agents import Agent, Runner",
        "",
        "# NOTE: models below are the folder's Claude ids; on OpenAI map them to a gpt-* id.",
        "",
    ]
    roster = [a for a in project.agents if not a.subagents]
    coordinators = [a for a in project.agents if a.subagents]
    for a in roster:
        out += _openai_agent_block(a, o, is_coordinator=False)
    for a in coordinators:
        out += _openai_agent_block(a, o, is_coordinator=True)
    root = coordinators[0] if coordinators else (project.agents[0] if project.agents else None)
    if root is not None:
        out += [
            "",
            "async def main():",
            f'    result = await Runner.run({_ident(root.name)}, "your task here")',
            "    print(result.final_output)",
            "",
            "",
            'if __name__ == "__main__":',
            "    asyncio.run(main())",
        ]
    return {"agent.py": "\n".join(out) + "\n"}


def _openai_agent_block(a, o, is_coordinator: bool) -> list:
    lines = [f"# --- agent: {a.name} ---"]
    used_sandbox = (a.builtin_tools is None) or bool(set(a.builtin_tools or []) & _SANDBOX_TOOLS)
    if used_sandbox:
        lines.append(f"# DEGRADED (built-in sandbox): {o['builtin_sandbox']['reason']}")
    asks = [t for t, p in (a.builtin_tool_policies or {}).items() if p == "ask"]
    asks += [f"{m.name}.{t}" for m in a.mcp_servers for t, p in (m.tool_policies or {}).items() if p == "ask"]
    if asks:
        lines.append(f"# approvals are client-side here (:ask on {', '.join(asks)})")
    if a.skills:
        lines.append(f"# skills ({', '.join(s.name for s in a.skills)}): upload via the Skills API and attach by id")
    if is_coordinator:
        lines.append("#   subagents below are exposed as tools (agent-as-tool); the delegation loop runs in THIS process")
    tool_lines = []
    for m in a.mcp_servers:
        if m.transport != "url":
            tool_lines.append(f"        # SKIPPED stdio MCP '{m.name}' - needs a URL MCP server")
        else:
            tool_lines.append(f"        # MCP '{m.name}' ({m.url}): attach via HostedMCPTool / MCP server params")
    if is_coordinator:
        for sub in a.subagents:
            tool_lines.append(
                f'        {_ident(sub)}.as_tool(tool_name="ask_{sub.replace("-", "_")}", '
                f'tool_description="Delegate to the {sub} sub-agent."),'
            )
    lines.append(f"{_ident(a.name)} = Agent(")
    lines.append(f"    name={a.name!r},")
    lines.append(f"    model={a.model!r},")
    lines.append(f"    instructions={_py(a.system)},")
    lines.append("    tools=[")
    lines += tool_lines
    lines.append("    ],")
    lines.append(")")
    lines.append("")
    return lines
